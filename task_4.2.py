# Найдите выручку компании в зависимости от месяца
# Для этого напишите функцию, которая на вход принимает
# список с датами и список с выручкой, а на выходе словарь,
# где ключи - это месяцы, а значения - это выручка.
# Используйте аннотирование типов.

from typing import List, Dict
import datetime

def calculate_revenue_by_month(dates: List[str], revenue: List[int]) -> Dict[str, int]:
    revenue_by_month = {}
    for i in range(len(dates)):
        date = datetime.datetime.strptime(dates[i], '%Y-%m-%d')
        month = date.strftime('%m-%Y')
        if month in revenue_by_month:
            revenue_by_month[month] += revenue[i]
        else:
            revenue_by_month[month] = revenue[i]
    return revenue_by_month
# Разберем этот код подробно:

# В первых строках мы импортируем необходимые модули, чтобы работать со списками, словарями и датами.
# Далее мы определяем функцию calculate_revenue_by_month, которая принимает на вход два аргумента: список дат dates и список сумм покупок revenue, и возвращает словарь revenue_by_month.
# В первой строке функции мы создаем пустой словарь revenue_by_month, который будет содержать информацию о выручке по месяцам.
# Далее мы перебираем значения в списках dates и revenue с помощью цикла for.
# В каждой итерации цикла мы преобразуем дату из строкового формата в формат datetime с помощью функции strptime.
# Затем мы преобразуем дату в строку с месяцем и годом, используя метод strftime.
# Далее мы проверяем, содержит ли словарь ключ с текущим месяцем month.
# Если ключ уже есть, то увеличиваем значение выручки на значение из списка revenue.
# Если ключа в словаре нет, то мы создаем его и присваиваем значение из списка revenue.
# После завершения цикла мы возвращаем словарь с информацией о выручке по месяцам.
# Пример использования функции:

